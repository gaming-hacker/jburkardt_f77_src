c  Reference:
c
c    Helmut Michels,
c    The Data Plotting Software DISLIN - version 10.4,
c    Shaker Media GmbH, January 2010,
c    ISBN13: 978-3-86858-517-9.
c
      implicit none

      integer n_max
      parameter ( n_max = 299 )

      integer i
      integer m
      integer n
      integer pat
      real r
      real x(n_max)
      real xy(2,n_max)
      real y(n_max)
      real zero(n_max)

      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'GEYSER:'
      write ( *, '(a)' ) '  FORTRAN77 version'
      write ( *, '(a)' ) '  Use DISLIN routines to make a bar chart.'
c
