        xmid=(xmin+xmax)/2.0d0
        ymid=(ymin+ymax)/2.0d0
        do i=1,nvf
            vx(i)=0.5d0+scale*(vx(i)-xmid)
            vy(i)=0.5d0+scale*(vy(i)-ymid)
        enddo
c
        ip(1)=0
        ip(2)=nvf
        ip(3)=0
        ip(4)=nbf
        rp(15)=hmax
        rp(16)=grade
c
c       create itnode
c
        call sklutl(0,vx,vy,xm,ym,itnode,ibndry,ip,w,iflag)
        return
        end
c-----------------------------------------------------------------------
c
c             piecewise linear triangle multi grid package
c
c                    edition 9.0 - - - march, 2004
c
c-----------------------------------------------------------------------
        subroutine lcc(ic,jc,iv,jv,ix,iy,iptr,width,ihsp)
c
            implicit double precision (a-h,o-z)
            implicit integer (i-n)
            integer
     +          ix(*),iy(*),width(*),iptr(*)
c
c       we assume that char jc is following char ic in the message
c
        jv=0
        iv=0
        jshift=width(jc)+ihsp
        ll=(width(ic)+width(jc)+ihsp)**2+500**2
c
        do i=iptr(ic),iptr(ic+1)-1
            do j=iptr(jc),iptr(jc+1)-1
                kk=(ix(i)+jshift-ix(j))**2+(iy(i)-iy(j))**2
                if(kk.lt.ll) then
                    iv=i-iptr(ic)+1
                    jv=j-iptr(jc)+1
                    ll=kk
                endif
            enddo
        enddo
        return
        end
c-----------------------------------------------------------------------
c
c             piecewise linear triangle multi grid package
c
c                    edition 9.0 - - - march, 2004
c
c-----------------------------------------------------------------------
        subroutine lcb(ic,lr,iv,ix,iy,iptr,width,ibord)
c
            implicit double precision (a-h,o-z)
            implicit integer (i-n)
            integer
     +          ix(*),iy(*),width(*),iptr(*)
c
c       distance from ic to boundary
c
        iv=0
        if(lr.eq.1) then
            jshift=width(ic)+ibord
        else
            jshift=-(width(ic)+ibord)
        endif
        ll=(width(ic)+iabs(jshift))**2+500**2
c
        iv=0
        do i=iptr(ic),iptr(ic+1)-1
            kk=(ix(i)-jshift)**2+iy(i)**2
            if(kk.lt.ll) then
                iv=i-iptr(ic)+1
                ll=kk
            endif
        enddo
        return
        end
