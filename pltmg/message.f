        subroutine fstr1(ss,length,sval,map)
c
            implicit double precision (a-h,o-z)
            implicit integer (i-n)
            integer
     +          map(*)
            character*1
     +          blank,dbleq
            character*80
     +          ss,sval
            save blank,dbleq,mxchar
            data blank,dbleq,mxchar/' ','"',80/
c
        istart=mxchar+1
        istop=0
        ss=' '
        do i=1,mxchar
            jj=ichar(sval(i:i))
            if(map(jj).ne.88) then
                istart=min0(istart,i)
                istop=max0(istop,i)
            endif
        enddo
        if(istart.gt.istop) then
            length=0
        else
            length=istop-istart+1
            ss(1:length)=sval(istart:istop)
        endif
        return
        end
